"""### **Cell 6 — Validate Image Folder**
* This cell scans a specific folder to find and verify compatible image files.
* It expects a folder path containing JPG, PNG, or WebP files.
* It outputs a list of valid image paths and reports any corrupted files found.
"""

SUPPORTED_FORMATS = {'.jpg', '.jpeg', '.png', '.webp', '.bmp'}
IMAGE_FOLDER = '/content/test_images'

def load_valid_images(folder):
    valid = []
    skipped = []
    for f in sorted(Path(folder).iterdir()):
        if f.suffix.lower() in SUPPORTED_FORMATS:
            try:
                img = Image.open(f).convert('RGB')
                img.verify()
                valid.append(str(f))
            except Exception as e:
                skipped.append((f.name, str(e)))
    print(f"Valid images: {len(valid)}")
    if skipped:
        print(f"Skipped {len(skipped)} corrupted files: {[s[0] for s in skipped]}")
    return valid

image_paths = load_valid_images(IMAGE_FOLDER)

"""### **Cell 7 — Single Embedding Logic**
* This cell defines a function to extract a balanced foreground and background embedding from an image.
* It uses the 'alpha' parameter to adjust the balance between the main subject and the scene.
* It outputs a 384-dimensional numerical vector representing the image.
"""

def extract_embedding(image_path, alpha=0.5):
    """
    Extracts a combined FG/BG embedding from one image.

    alpha controls the FG/BG balance:
      0.0 = pure CLS token (global scene / background)
      1.0 = pure patch average (foreground objects)
      0.5 = equal blend (default — recommended)
    """
    img = Image.open(image_path).convert('RGB')
    inputs = processor(images=img, return_tensors='pt')
    inputs = {k: v.to(device) for k, v in inputs.items()}

    with torch.no_grad():
        outputs = model(**inputs)

    tokens = outputs.last_hidden_state  # shape: [1, 257, 384]
                                        # token 0 = CLS, tokens 1-256 = patches

    cls_token   = tokens[:, 0, :]       # [1, 384] — global/BG signal
    patch_tokens = tokens[:, 1:, :]     # [1, 256, 384] — patch/FG signal
    patch_avg   = patch_tokens.mean(dim=1)  # [1, 384]

    # Weighted combination
    combined = (1 - alpha) * cls_token + alpha * patch_avg  # [1, 384]

    # L2 normalize for cosine similarity later
    combined = torch.nn.functional.normalize(combined, dim=-1)

    return combined.squeeze().cpu().numpy()  # [384]


