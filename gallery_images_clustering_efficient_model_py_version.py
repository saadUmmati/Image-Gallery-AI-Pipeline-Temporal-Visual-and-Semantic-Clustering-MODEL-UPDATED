def compute_centroids(embeddings, cluster_labels, n_clusters):
    """Compute mean embedding per cluster."""
    centroids = {}
    for cid in range(n_clusters):
        mask = cluster_labels == cid
        if mask.sum() > 0:
            centroids[cid] = embeddings[mask].mean(axis=0)
    return centroids

