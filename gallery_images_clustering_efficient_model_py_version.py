"""### **Cell 31 — Detect Digital Content**
* This cell uses fast pixel rules to identify screenshots, documents, and memes.
* It analyzes image brightness, color variance, and aspect ratio.
* It outputs a percentage breakdown of your gallery's 'special' vs 'photo' content.
"""

import numpy as np
from PIL import Image, ImageStat

def classify_image_type(image_path):
    """
    Fast pixel-level pre-classifier. Returns:
    'screenshot'  — UI elements, status bars, high text density
    'document'    — scanned docs, receipts, white background + text
    'meme'        — image with large text overlay
    'wallpaper'   — high resolution, very wide aspect ratio
    'photo'       — normal camera photo (passes to CLIP naming)
    """
    try:
        img = Image.open(image_path).convert('RGB')
        w, h = img.size
        aspect = w / h

        # Convert to grayscale for analysis
        gray = img.convert('L')
        stat = ImageStat.Stat(gray)
        mean_brightness = stat.mean[0]
        std_brightness  = stat.stddev[0]

        # Rule 1 — Screenshot detection
        # Screenshots have exact device resolution ratios + very uniform edges
        common_ratios = [9/16, 9/19.5, 9/20, 9/21, 3/4, 2/3]
        is_ratio_match = any(abs(w/h - r) < 0.05 or abs(h/w - r) < 0.05
                            for r in common_ratios)

        # Check top/bottom strips for uniform color (status bar pattern)
        top_strip    = np.array(img.crop((0, 0, w, min(60, h//8))))
        bottom_strip = np.array(img.crop((0, max(0, h - 60), w, h)))
        top_uniform    = top_strip.std() < 18
        bottom_uniform = bottom_strip.std() < 18

        if is_ratio_match and (top_uniform or bottom_uniform) and std_brightness < 70:
            return 'screenshot'

        # Rule 2 — Document detection
        # Very bright background, low color variance, portrait orientation
        pixel_array = np.array(img)
        color_std = pixel_array.std(axis=(0,1)).mean()
        if mean_brightness > 210 and color_std < 30 and h > w:
            return 'document'

        # Rule 3 — Wallpaper detection
        # Very wide or very tall, high resolution
        if (aspect > 1.9 or aspect < 0.5) and (w * h > 2_000_000):
            return 'wallpaper'

        # Rule 4 — Meme detection
        # High contrast black/white regions + standard meme ratios
        white_ratio = (np.array(gray) > 240).mean()
        black_ratio = (np.array(gray) < 15).mean()
        if white_ratio > 0.25 and black_ratio > 0.05:
            return 'meme'

        return 'photo'

    except Exception:
        return 'photo'


