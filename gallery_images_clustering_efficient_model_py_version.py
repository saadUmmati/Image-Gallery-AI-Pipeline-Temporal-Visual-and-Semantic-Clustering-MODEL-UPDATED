"""### **Cell 13 — Determine Optimal Clusters**
* This cell tests different numbers of groups to find the most natural fit for your images.
* It uses the 'Silhouette Score' to measure how well-separated the groups are.
* It outputs a recommendation for the 'Optimal K' (number of clusters).
"""

from sklearn.cluster import KMeans
from sklearn.metrics import silhouette_score

n_images = len(image_paths)
max_k = min(20, n_images // 2)
min_k = 2

print(f"Testing K from {min_k} to {max_k}...")

scores = {}
for k in range(min_k, max_k + 1):
    km = KMeans(n_clusters=k, random_state=42, n_init=10)
    labels = km.fit_predict(embeddings_32d)
    score = silhouette_score(embeddings_32d, labels, metric='euclidean')
    scores[k] = score
    print(f"  K={k:2d}  silhouette={score:.4f}")

optimal_k = max(scores, key=scores.get)
print(f"\n✓ Optimal K = {optimal_k}  (score: {scores[optimal_k]:.4f})")

"""### **Cell 14 — Execute Final Clustering**
* This cell performs the final grouping of images based on the optimal number found previously.
* It uses the K-Means algorithm to assign every image a specific cluster ID.
* It outputs the final distribution of images across all generated groups.
"""

