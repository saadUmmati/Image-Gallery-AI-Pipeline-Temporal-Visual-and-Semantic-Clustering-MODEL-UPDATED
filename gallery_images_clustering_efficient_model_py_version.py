"""### **Cell 12 — UMAP Dimensionality Reduction**
* This cell compresses the 384-dimensional data into 32 dimensions for better clustering results.
* It uses the UMAP algorithm with a 'cosine' metric to preserve visual relationships.
* It outputs two versions of the data: one for calculations and one 2D version for mapping.
"""

import umap

print("Running UMAP reduction (384 → 32 dims)...")
reducer = umap.UMAP(
    n_components=32,       # compress to 32 dims for clustering
    n_neighbors=15,        # local neighborhood size
    min_dist=0.0,          # tighter clusters (0.0 = best for clustering use)
    metric='cosine',       # matches our L2-normalized embeddings
    random_state=42
)

embeddings_32d = reducer.fit_transform(embeddings)
print(f"Reduced shape: {embeddings_32d.shape}")   # [N, 32]

# Also reduce to 2D separately just for visualization later
reducer_2d = umap.UMAP(
    n_components=2,
    n_neighbors=15,
    min_dist=0.05,
    metric='cosine',
    random_state=42
)
embeddings_2d = reducer_2d.fit_transform(embeddings)
print(f"2D shape for viz: {embeddings_2d.shape}")  # [N, 2]

np.save('/content/embeddings_32d.npy', embeddings_32d)
np.save('/content/embeddings_2d.npy', embeddings_2d)
