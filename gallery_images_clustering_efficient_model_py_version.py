"""### **Cell 38 — Create Android Wrapper**
* This cell wraps the DINOv2 model in a special class that outputs a single normalized vector.
* This simplifies the Android code, as the app only has to handle one input and one output.
* It outputs a confirmation of the 'Wrapper output shape' and normalization.
"""

import torch.nn as nn

class DINOv2EmbedderWrapper(nn.Module):
    """
    Wraps DINOv2-small to output a single L2-normalized 384-dim
    FG/BG-combined embedding per image.

    Input  : float32 [1, 3, 224, 224]  — normalized image tensor
    Output : float32 [1, 384]          — L2-normalized embedding
    """
    def __init__(self, dinov2_model, alpha=0.5):
        super().__init__()
        self.model = dinov2_model
        self.alpha = alpha   # FG/BG blend: 0=pure BG, 1=pure FG, 0.5=balanced

    def forward(self, pixel_values):
        outputs = self.model(pixel_values=pixel_values)
        tokens  = outputs.last_hidden_state      # [1, 257, 384]

        cls_token    = tokens[:, 0, :]           # [1, 384] — BG signal
        patch_avg    = tokens[:, 1:, :].mean(1)  # [1, 384] — FG signal

        combined = (1 - self.alpha) * cls_token + self.alpha * patch_avg
        combined = nn.functional.normalize(combined, dim=-1)  # L2 normalize
        return combined                          # [1, 384]


wrapper = DINOv2EmbedderWrapper(model, alpha=0.5)
