def get_image_clip_embedding(image_path):
    """Get CLIP visual embedding for a single image."""
    try:
        img = Image.open(image_path).convert('RGB')
        tensor = clip_preprocess(img).unsqueeze(0).to(device)
        with torch.no_grad():
            feat = clip_model.encode_image(tensor)
            feat = feat / feat.norm(dim=-1, keepdim=True)
        return feat.squeeze().cpu().numpy()
    except:
        return None

def name_cluster(paths, pre_type_map):
    """
    Names a cluster using:
    1. Pre-type override (screenshots/documents/memes/wallpapers)
    2. CLIP cosine similarity for photos
    """
    # Check if majority of images are a special type
    types = [pre_type_map.get(p, 'photo') for p in paths]
    type_counts = {}
    for t in types:
        type_counts[t] = type_counts.get(t, 0) + 1
    majority_type = max(type_counts, key=type_counts.get)
    majority_pct  = type_counts[majority_type] / len(types)

    # If >60% are a special digital type, override immediately
    special_type_names = {
        'screenshot': ('Screenshots', 1.0),
        'document':   ('Documents',   1.0),
        'meme':       ('Memes',       1.0),
        'wallpaper':  ('Wallpapers',  1.0),
    }
    if majority_type in special_type_names and majority_pct > 0.60:
        name, score = special_type_names[majority_type]
        return name, score, "HIGH", []

    # CLIP cosine similarity naming for photos
    sample = paths[:min(SAMPLE_PER_CLUSTER, len(paths))]
    clip_embeddings = [get_image_clip_embedding(p) for p in sample]
    clip_embeddings = [e for e in clip_embeddings if e is not None]

    if not clip_embeddings:
        return None, 0.0, "LOW", []

    # Average visual embedding for the cluster
    avg_clip = np.mean(clip_embeddings, axis=0)
    avg_clip = avg_clip / (np.linalg.norm(avg_clip) + 1e-8)

    # Cosine similarity against all category embeddings
    sims = cosine_similarity([avg_clip], category_matrix)[0]  # [N_cats]

    top3_idx  = sims.argsort()[-3:][::-1]
    top_idx   = top3_idx[0]
    top_score = sims[top_idx]
    top_name  = category_names[top_idx]
    top3      = [(category_names[i], round(float(sims[i]), 3)) for i in top3_idx]

    if top_score >= SIMILARITY_THRESHOLD:
        confidence = "HIGH" if top_score > 0.28 else "MEDIUM"
        return top_name, float(top_score), confidence, top3
    else:
        return None, float(top_score), "LOW", top3


