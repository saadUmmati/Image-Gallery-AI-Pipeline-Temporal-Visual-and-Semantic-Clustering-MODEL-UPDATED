    with open(state_path, 'rb') as f:
        state = pickle.load(f)

    centroids      = state['centroids']
    cluster_labels = state['cluster_labels'].tolist()
    all_embeddings = list(state['embeddings'])
    all_paths      = list(state['image_paths'])
    cluster_meta   = state['cluster_meta']
    cluster_names  = state['cluster_names']
    next_cid       = state['next_cluster_id']
    fallback_idx   = state['next_fallback_idx']

    assigned_new  = []
    created_new   = []
    centroid_keys = sorted(centroids.keys())
    centroid_mat  = np.array([centroids[k] for k in centroid_keys])

    print(f"\nProcessing {len(new_image_paths)} new images...")

    for path in tqdm(new_image_paths, desc="Incremental"):
        if path in all_paths:
            print(f"  Skip (already processed): {Path(path).name}")
            continue

        # Extract DINOv2 embedding
        try:
            img    = Image.open(path).convert('RGB')
            inputs = dinov2_processor(images=img, return_tensors='pt')
            inputs = {k: v.to(device) for k, v in inputs.items()}
            with torch.no_grad():
                out = dinov2_model(**inputs)
            emb = out.last_hidden_state[:, 0, :] * 0.5 + \
                  out.last_hidden_state[:, 1:, :].mean(dim=1) * 0.5
            emb = torch.nn.functional.normalize(emb, dim=-1)
            emb = emb.squeeze().cpu().numpy()
        except Exception as e:
            print(f"  Failed: {Path(path).name} — {e}")
            continue

        # Compare against all cluster centroids
        sims   = cosine_similarity([emb], centroid_mat)[0]
        max_sim = sims.max()
        best_cid = centroid_keys[sims.argmax()]

        if max_sim >= match_threshold:
            # Assign to best matching existing cluster
            cluster_labels.append(best_cid)
            cluster_meta[str(best_cid)]['image_paths'].append(path)
            cluster_meta[str(best_cid)]['count'] += 1

            # Update centroid (running mean)
            old_n = cluster_meta[str(best_cid)]['count'] - 1
            centroids[best_cid] = (centroids[best_cid] * old_n + emb) / (old_n + 1)

            assigned_new.append((path, best_cid, max_sim))
            print(f"  ✓ '{Path(path).name}' → Cluster {best_cid} "
                  f"('{cluster_names[str(best_cid)]['name']}') sim={max_sim:.3f}")

        else:
            # Create new cluster
            new_cid     = next_cid
            next_cid   += 1
            cid_str     = str(new_cid)

            # Name it
            pre_type    = classify_image_type(path)
            name, score, conf, top3 = name_cluster([path], {path: pre_type})
            if name is None:
                name = f"Cluster {fallback_idx}"
                fallback_idx += 1

            centroids[new_cid] = emb
            cluster_labels.append(new_cid)
            cluster_meta[cid_str] = {
                'image_paths': [path],
                'count': 1,
                'name': name
            }
            cluster_names[cid_str] = {
                'name': name, 'score': score,
                'confidence': conf, 'top3': top3, 'count': 1
            }
            created_new.append((path, new_cid, name))
            print(f"  ★ '{Path(path).name}' → NEW cluster {new_cid} "
                  f"('{name}') best_sim={max_sim:.3f}")

        all_embeddings.append(emb)
        all_paths.append(path)

    # Save updated state back to disk
    state.update({
        'centroids':        centroids,
        'cluster_labels':   np.array(cluster_labels),
        'embeddings':       np.array(all_embeddings),
        'image_paths':      all_paths,
        'cluster_meta':     cluster_meta,
        'cluster_names':    cluster_names,
        'next_cluster_id':  next_cid,
        'next_fallback_idx': fallback_idx
    })
    with open(state_path, 'wb') as f:
        pickle.dump(state, f)

    print(f"\n── Incremental Summary ──")
    print(f"  Assigned to existing clusters : {len(assigned_new)}")
    print(f"  New clusters created          : {len(created_new)}")
    print(f"  Total clusters now            : {next_cid}")
    print(f"  State saved ✓")

    return assigned_new, created_new


