"""### **Cell 15 — Analyze Foreground Dominance**
* This cell determines if a cluster is dominated by specific objects or broad backgrounds.
* It samples images from each group to check the signal strength of individual pixels.
* It outputs a label of 'FOREGROUND' or 'BACKGROUND' for every cluster.
"""

from sklearn.metrics.pairwise import cosine_similarity
import torch
from transformers import AutoImageProcessor, AutoModel
from PIL import Image

print("Computing FG/BG dominance per cluster...")

# We use the CLS vs patch token ratio as the FG/BG signal
# CLS-heavy = background/scene dominant
# Patch-heavy = foreground object dominant

def get_fg_bg_ratio(image_path):
    img = Image.open(image_path).convert('RGB')
    inputs = processor(images=img, return_tensors='pt')
    inputs = {k: v.to(device) for k, v in inputs.items()}
    with torch.no_grad():
        outputs = model(**inputs)
    tokens = outputs.last_hidden_state
    cls_norm   = tokens[:, 0, :].norm(dim=-1).item()
    patch_norm = tokens[:, 1:, :].mean(dim=1).norm(dim=-1).item()
    ratio = patch_norm / (cls_norm + 1e-8)
    return ratio  # >1.0 = FG dominant, <1.0 = BG dominant

cluster_fg_bg = {}
for cid, paths in clusters.items():
    sample = paths[:min(5, len(paths))]  # sample up to 5 per cluster
    ratios = [get_fg_bg_ratio(p) for p in sample]
    avg_ratio = np.mean(ratios)
    dominance = "FOREGROUND" if avg_ratio >= 1.0 else "BACKGROUND"
    cluster_fg_bg[cid] = {"ratio": avg_ratio, "dominance": dominance}
    print(f"  Cluster {cid:2d}: ratio={avg_ratio:.3f}  → {dominance}")

