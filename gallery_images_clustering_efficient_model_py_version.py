"""### **Cell 20 — Calculate Quality Scores**
* This cell computes mathematical quality metrics for each individual group.
* It measures 'Cohesion' (similarity within a group) and 'Silhouette' (separation from others).
* It outputs a 'GOOD', 'WEAK', or 'POOR' status for every cluster found.
"""

from sklearn.metrics.pairwise import cosine_similarity
from sklearn.metrics import silhouette_samples

print("Computing per-cluster quality scores...\n")

# Silhouette score per sample
sample_scores = silhouette_samples(
    np.load('/content/embeddings_32d.npy')[valid_mask],
    cluster_labels
)

quality_report = {}
for cid_str, meta in cluster_meta.items():
    cid = int(cid_str)
    mask = cluster_labels == cid

    # Intra-cluster cosine similarity (cohesion)
    cluster_embs = embeddings[mask]
    if len(cluster_embs) > 1:
        sim_matrix = cosine_similarity(cluster_embs)
        np.fill_diagonal(sim_matrix, 0)
        avg_sim = sim_matrix.sum() / (len(cluster_embs) * (len(cluster_embs) - 1))
    else:
        avg_sim = 1.0

    # Average silhouette for this cluster
    avg_sil = sample_scores[mask].mean()

    quality_report[cid] = {
        "cohesion": round(float(avg_sim), 4),
        "silhouette": round(float(avg_sil), 4),
        "count": int(mask.sum()),
        "dominance": meta['fg_bg_dominance']
    }

    status = "✓ GOOD" if avg_sil > 0.3 else ("⚠ WEAK" if avg_sil > 0.1 else "✗ POOR")
    print(f"Cluster {cid:2d}: cohesion={avg_sim:.3f}  "
          f"silhouette={avg_sil:.3f}  {status}  "
          f"[{meta['fg_bg_dominance']}]  {mask.sum()} imgs")

# Overall score
overall = np.mean([v['silhouette'] for v in quality_report.values()])
print(f"\nOverall clustering quality: {overall:.4f}")
